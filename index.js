function theBeatlesPlay(musicians, instruments){
  var array = [];
  for(let i = 0; array.length < 4; i++){
    array.push(musicians[i] + " plays " + instruments[i])
  }
  return array;
};

function johnLennonFacts(facts){
  var array = [];
  while(facts.indexOf !== -1){
    array.push(facts[0] + "!!!");
    facts++;
  }
  return array;
}
